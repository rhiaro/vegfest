<h2 class="pink">
Location
</h2>
<p>
<a href="http://www.kirkcaldyoldkirk.org.uk/">The Kirkcaldy Old Kirk</a> is a warm and welcoming events space in a historic building just off the High Street.
</p>
<p>
VegFest will take place on the ground floor, with stalls and activities in the main space, and talks and workshops in the back room.
</p>
<p>
Well-behaved dogs are welcome.
</p>
<h2 class="pink">
Accessibility
</h2>
<p>
The Old Kirk can be accessed by a cascading staircase or steep steps from Kirk Wynd (KY1 1EH) or through the level drive from Oswald's Wynd and Church Lane, which provides disabled access (KY1 1BG).
</p>
<p>
There are accessible toilets, and plenty of seating.
</p>
<p>
Restricted parking is available close to the doors in the graveyard for those with limited mobility. Please contact us to arrange this in advance. People can also be dropped off and picked up close to the doors, without needing a parking spot.
</p>
<h2 class="pink">
Getting here
</h2>
<p>
The bus station is a short walk (5 minutes) from the church and is situated behind the Town House at the end of Hunter Street. The railway station is 7 minutes walk from the Old Kirk along Hunter Street from the Town House and through the War Memorial Gardens past the Kirkcaldy Galleries.
</p>
<p>
A <a href="https://en.parkopedia.co.uk/parking/carpark/oswald_wynd/ky1/kirkcaldy/">public car park</a> is situated at the foot of Church Lane with a short walk along the lane to the church. This is free up to 1pm on Sundays. Several other carkparks are also within a short walking distance.
</p>
