<h2 class="tan">
Fife VegFest 2
</h2>
<p><strong>This is our second event showcasing local vegan-friendly businesses</strong>.</p>
<p>
We wanted to build on the success of the <a href="vegfest1">first Fife VegFest</a> in a new venue, the Old Kirk in Kirkcaldy, that allows us to have more stall holders and give even more people access to the amazing vegan food and drink that’s made locally in Fife, and slightly further afield.
</p>
<p>
We are keen to promote <a href="who">local vegan businesses</a>, and give people an insight into healthy, ethical and sustainable food choices, and anyone who may be trying out Veganuary.
</p>
<p>
There will be local artists running a creative corner, including badge making and block printing your very own Fife VegFest merchandise - so make sure to bring along a t-shirt, tote bag or patch you want to customise anything - and there will also be <a href="#timetable">panels</a> and a yoga workshop.
</p>
<p>
There'll also be a stall with Plant Based Fife, a local group who empowers the vegan community with resources, events and advocacy for sustainable, plant-based living, and are currently calling on organisations to commit to the <a href="https://plantbasedtreaty.org/">plant-based treaty</a>. They'll be joined by local sustainability and social justice charity <a href="https://greenerkirkcaldy.org.uk">Greener Kirkcaldy</a> and global environmental campaigners <a href="https://greenpeace.org">Greenpeace</a>.
</p>
<p>
There will be a raffle running during the day, with <a href="#raffle">prizes generously donated</a> by our vendors, and all proceeds go back into running future VegFests.
</p>
<p>
Fife VegFest is organised and run entirely by volunteers and not for-profit!
</p>
