    <h2 class="tan">
Second VegFest... bigger and better.
</h2>
<p>
An incredible turnout for our second ever VegFest!
</p>
<p>
In a bigger venue, with more stalls, we were delighted to see <strong>around 450 people</strong> over the course of the day, with tons of Kirkcaldy locals, visitors from wider Fife, as well as people travelling from Edinburgh, Perthshire and Dundee. Once again several vendors sold out early, and we showed there's a <strong>real interest in vegan food in Kirkcaldy, and no shortage of local businesses willing and able to supply it</strong>.
</p>
<p style="width:100%;"><img src="crowd.jpg" alt="Crowds of people at stalls at an indoor market, with stained glass windows around." /></p>
<p style="font-size:1em">Photo by <a href="https://www.doubtlesshouse.org.uk/">James Baster</a>.</em></p>
<p>
We welcomed back The Tiffin, Hughes Bakery, Ecobean, Lochaber Farm and Bodylushious, and were thrilled to be joined by new vendors (but old favourites) QBee Rustique, Bliss Bites, East Neuk Seaweed, Aberdour Jams & Chutneys, Raw Wild & Conscious, Fido Luxury Dog Treats, and Kama Vegan Bakes, and informational stalls from Plant Based Fife, Greener Kirkcaldy and Working For Wildlife (on behalf of Greenpeace).
</p>
<p style="width:100%;"><img src="treats.jpg" alt="A composite image. Left: a hand holding two large dog biscuits stamped with VEGFEST against a neon pink sign saying 'Fido'. Top right: a display of cakes, with frosted cupcakes in the foreground (QBee). Bottom right: Indian couple Tony and Priya in black aprons pose behind their stall with a big sign saying The Tiffin." title="Local vegan Kirkcaldy businesses, Fido Luxury Dog Treats, QBee Rustique and The Tiffin at Fife VegFest" /></p>
<p style="font-size:1em"><em>Local vegan Kirkcaldy businesses, Fido Luxury Dog Treats, QBee Rustique and The Tiffin at Fife VegFest. Photos by <a href="https://www.doubtlesshouse.org.uk/">James Baster</a>.</em></p>
<p>
We loved the space in the Old Kirk, who provided teas and coffees and a quiet place to sit out of the way of the hustle and bustle.
</p>
<p>
We held two panels and a yin yoga class in the back room. Local artists Lea and Abi again hosted the Creative Space, this time with the option to block print your own VegFest merchandise, including tshirts, tote bags and patches.
</p>
<p>
Extra special thanks to The Tiffin, 269 Vegan, Kitchen Treasury, Ecobean, Lochaber Bakery, Aberdour Jams & Chutneys, CLEAR, Raw Wild & Conscious, Bodylushious and Couple O Mugs for their donations to the raffle. All of the proceeds from the raffle and sale of VegFest merch go straight back into covering our costs for running the event.
</p>
<p style="width:100%;"><img src="raffle.jpg" alt="A composite image. Top left: a rack of white tshirts with Fife VegFest 'weird veg' mascots printed on them in bright colours. Top right: A white shirt stamped with FIFE VEGFEST hangs in the foreground, with people talking in the background. Bottom: A table of raffle prizes from above, including a basket with bread, jam, juice and treats, a pink heart shaped cake, vouchers, and other boxes of goodies." title="Prizes generously donated by vendors and other local businesses." /></p>
<p style="font-size:1em">Photos by <a href="https://www.doubtlesshouse.org.uk/">James Baster</a>.</em></p>
<p>
We had a fantastic team of volunteers who worked incredibly hard at setting up, welcoming people, running errands, signposting, selling raffle tickets, and helping in the Creative Space, as well as packing up and cleaning afterwards. Special thanks to Leila, Kit, James, Jonathan, Diane, Kirstin, Abi, Jodiee, Nate, Ellen, Penny.
</p>
<p>
We had a couple of obstacles in the run up, and our vendors, helpers and team really pulled together and went above and beyond to make the day a success.
</p>
<p>
We're already planning VegFest 3. Think sea, sun, bigger and better. <a href="https://vgn.kdy.scot/getinvolved">Find out how you can get involved</a>, keep an eye on our mailing list and social media, and watch this space.
</p>
<p>
With love,
</p>
<p>
Team VegFest (Amy, Lea, Tony & Shiva)
