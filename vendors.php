<h2 class="orange">
Who is this for?
</h2>
<p>
We're aiming to include a selection of Fife-based businesses who offer excellent choices for people following a vegan diet or lifestyle.
</p>
<p>
We'd also like to include businesses and organisations who may not be specifically targeting vegans, but who operate with a compatible ethos - for example, with compassion for others, holistic wellbeing, or sustainability at their heart.
</p>
<p>
If this is you - or if this is something you'd like to move towards - we'd love to hear from you.
</p>
<h2 class="orange">
What will it cost?
</h2>
<p>
We're asking that stallholders contribute up to &pound;20 to cover the cost of hiring the venue (which is itself a charity). This figure will be lower the more stalls commit to joining us! We're not trying to make any profit. This will be payable by bank transfer.
</p>
<p>
There will be no charge for the public entering the venue; they can spend every penny on your delicious treats.
</p>
<h2 class="orange">
What's in it for me?
</h2>
<p>
We'd love for you to show off your vegan food offerings to <strong>raise your profile</strong> among Fifers with dietary restrictions and those making eco-conscious food choices. It can be hard for us to find good options - let us know that you can cater for us! And spend the day selling your fare to <strong>a new audience</strong>.
</p>
<p>
Our <a href="vegfest1">first ever VegFest</a> had over 300 people through the doors, and we had to close early due to everyone selling out.
</p>
<p>
This will also be an opportunity to <strong>network with and get inspiration</strong> from other small businesses facing similar challenges and opportunities, as well as representatives from local authorities and sustainability or food activism networks. And a chance to <strong>meet local foodies</strong> with ideas and enthusiasm for your vegan options.
</p>
<h2 class="orange">
What should we bring?
</h2>
<p>
If you're a food business, come along with <strong>vegan-friendly food and drinks</strong> that you regularly serve as part of your menu, or that you're thinking about dishing up in the near future and want to get some feedback on. (Please bring <em>only</em> vegan/plant-based products. We're happy to advise if you're not sure about something.)
</p>
<p>
Depending on what you offer, your goods may be prepackaged to take away, or you may want to "plate up" on demand. Food should be served in <strong>takeaway containers</strong> - please bring your own (along with napkins, cutlery, etc as appropriate).
</p>
<p>
For other businesses, please bring <strong>products to sell</strong>.
</p>
<p>
Please bring what you need to take payments (cash, card or both) and make your pricing clear. If possible, we'd encourage you to offer a few <strong>free samples</strong>, to entice those who aren't sure about vegan food or the cuisine or products you offer.
</p>
<p>
Please <strong>bring signage</strong> so people know who you are and where to find you. Do bring flyers, business cards, your vegan menu (or your full menu with vegan options highlighted) or similar to promote yourselves.
</p>
<h2 class="orange">
What facilities are available?
</h2>
<p>
The venue provides tables for the stalls, and chairs. There are no on-site cooking facilities. If you wish to serve hot or chilled food, please let us know and we will arrange for you to be near a power outlet. You will need to bring your own equipment for storing and serving.
</p>
<p>
If you wish to run a workshop or demonstration or other group session, we will have separate space avaialble. Let us know how much time you need, how many people you can accommodate, and what your setup will be, and we will add you into our timetable.
</p>
<p>
Parking for vendors is available for free close to the front door [TO BE CONFIRMED].
</p>
<h2 class="orange">
Why now?
</h2>
<p>
<a href="https://fccan.org.uk/">The Fife Climate Festival</a> takes place from the 21st September to 5th October. This is not a formal part of the programme, but we think it's a great opportunity to get people thinking about the impact of their food choices on the environment. And considering a vegan diet can be a big part of that!
</p>
<h2 class="orange">
Who is organising this?
</h2>
<p>
Amy, Tony, Amby and Lea are Kirkcaldy-based community-minded vegan foodies, who want to celebrate what our town and the wider area have, and to whet the local appetite for more. We want to support local businesses, and bring something positive to Kirkcaldy.
</p>
<h2 class="orange">
What else is happening?
</h2>
<p>
VegFest is mainly food-oriented, but we'll also have panel discussions, crafts, and other workshops. We'll be updating our website timetable information as we get it.
</p>
<h2 class="orange">
Now what?
</h2>
<p>
<a href="mailto:vegfest@kdy.scot">Send us an email</a> to let us know you'd like to join us. We're happy to answer any questions you have, and if you prefer to talk on the phone or in person just leave your contact information, and one of us will call you back or come out to meet you.
</p>
<p>
And if you can't make it, but would like to be involved in any future similar events, please let us know.
</p>
