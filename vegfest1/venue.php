<h2 class="pink">
Location
</h2>
<p>
<a href="https://www.kingstheatrekirkcaldy.com/">The King's Kirkcaldy</a> is a thriving intimate live venue situated on the esplanade of Kirkcaldy overlooking the River Forth.
</p>
<p>
VegFest will take place on the ground floor, with stalls and activities in the live lounge and lower rooms, and a seating area in the bar. Workshops will be in the upstairs rooms.
</p>
<p>
Well-behaved dogs are welcome.
</p>
<h2 class="pink">
Accessibility
</h2>
<p>
The ground floor is accessible via a wheelchair ramp, and there is an accessible toilet downstairs.
</p>
<p>
Some activities may take place in upstairs rooms, and unfortunately there is no lift.
</p>
<h2 class="pink">
Getting here
</h2>
<p>
The nearest train station is Kirkcaldy (KDY), about a 13 minute walk from the venue. Kirkcaldy bus station is about a 7 minute walk away, and served by direct buses from Edinburgh, Glasgow, St Andrews and Dundee, as well as all over Fife.
</p>
<p>
Pay and display car parking is available along the Esplanade and throughout the town center; most if it is free on Sundays.
</p>
