    <h2 class="green">First ever VegFest success!</h2>
    <p>
What an amazing turnout we had for our first ever Fife VegFest! <a href="https://www.fifetoday.co.uk/news/people/kirkcaldys-first-vegan-festival-a-huge-success-huge-turn-out-and-vendors-sell-out-4813760" target="_blank">Read about it in the Fife Free Press</a>.</p>
</p>
<p>
Thanks to everyone who spread the word, and came along on Sunday 6th October. We couldn't have done it without you.
</p>
<p>
It was a dreich and drizzly day in Kirkcaldy, but when our doors opened at 10 people immediately started coming in, with steady footfall all day. We had <strong>over 300 people</strong> through the doors!
</p>
<p>
Most were local, from Kirkcaldy and surrounding towns (Glenrothes, Burntisland, Leven, Crossgates, Cowdenbeath, Wemyss), and we had a good turnout from Dunfermline, Inverkeithing, Falkand, and the East Neuk, as well as people who travelled from Edinburgh, West Lothian, Glasgow and Dundee, and as far as Berwick-upon-Tweed!
</p>
<p>
We're thrilled to have thoroughly established that people will travel to Kirkcaldy for vegan food.
</p>
<p>
Half of our vendors had <strong>sold out before 2pm</strong>, and we were calling it a day by 3.
</p>
<p>
We'd like to thank <a href="vegfest1/who">our stallholders</a> who took a chance on us as a new market. We're so glad it was worth your while: Hughes Bakery (sold out by 12:45), Lochaber Bakery (13:00), Kitchen Treasury and Ecobean (13:10), Planted By Cali (13:20), and The Tiffin and Couple O Mugs who just about hung in until half past 2 feeding the hungry vegans who were still pouring in. Our non-food vendors also enjoyed successful days: thanks Bodylushious for skincare and candles, Betty Nicols who got dozens of entries into their plant-based menu competition, and Plant Based Fife who shared veganising tips and tricks, and local and national petitions for institutional change. You all made the day the success it was!
</p>
<p>
Our creative space (featuring a wee vegan library) was bustling all day, and particularly popular with kids. We also had two successful panel talks (Veganise Your Diet, and a general Vegan Q&A) which were well attended and vibrant.
</p>
<p>
We were successful on two fronts: bringing people into Kirkcaldy (a town we love) to support local businesses, <em>and</em> building a network of locals who are anywhere from curious to passionate about vegan food.
</p>
<p>
Local businesses also made connections, had a chance to learn from each other, and are already planning new collaborations.
</p>
<p>
We'd like to thank our excellent venue, <a href="https://www.kingstheatrekirkcaldy.com/">The King's</a>, who sold record numbers of teas and coffees from their bar. They ran out of oat milk twice. We're delighted that we also <strong>raised over Â£100 for them in donations</strong> at our front desk!
</p>
<p>
And last but not least, we're so grateful to Abi, Shiva, Leila and Pete who volunteered their time to go the extra mile in promoting the event, or whose help on the day was invaluable.
</p>
