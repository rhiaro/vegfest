  <h2 class="red">
VegFest 3.. whatever the weather
  </h2>
  <p>
<strong>Thanks to everyone who came out yesterday to make the third Fife VegFest such a massive success.</strong> Despite the challenges we had with the wind and rain, everyone really pulled together. We couldn't have got through the day without the good humour and resilience of our stallholders and team, absolutely stoic in the face of a normal Scottish summer.
  </p>
  <p>
In between the short but heavy downpours and big gusts of wind, we had a warm and sunny day! <strong>Kirkcaldy High Street was busier than many of us have seen it in a long time, with locals and visitors alike turning out even in the unpredictable weather.</strong> We were delighted to be colocated with <a href="https://vgn.kdy.scot/timetable#langtounfest">several other Lang Toun Fest events</a> in Kirkcaldy town centre, meaning there was plenty going on for a great day out!
  </p>
  <p>
Feedback on the diversity and quality of the food and products <a href="/vegfest3/who">on sale by local businesses</a> was fantastic, showing that <strong>there's a real appetite for this kind of event in Kirkcaldy</strong>.
  </p>
  <p>
While we were sorry to lose some of our vendors to the adverse conditions, many other sold out and packed up early, and the rest weren't phased and stuck it out until the very end!
  </p>
  <p>
Shoutouts to: Hughes Bakery for doughnuts selling out in half an hour, Earthy still processing a transaction under an airborne gazebo, Soft Serve Cartel for bringing the tunes and keeping the team caffeinated, East Neuk Seaweed for foregoing a gazebo entirely and working underwater, CLEAR's extensive setup of everything from refurbished bikes to Japanese maple trees, Greener Kirkcaldy for bringing locally grown veg and doing cooking demos in the pouring rain, and to QBee Rustique for being the last person standing!
  </p>
  <p>
A special mention to <a href="https://www.rawwildandconscious.com/">Raw, Wild and Conscious</a>, <a href="https://planetgfood.com/">Planet G</a>, and <a href="https://blissbitesscotland.co.uk/">Bliss Bites</a>, who got the worst of the wind damage. <strong>Please visit them online and look out for them at other markets to show them your support</strong>.
  </p>
  <p>
The Creative Space, hosted by <a href="https://everyonescotland.org/">Every One Every Day</a> inside the Mercat, was a big hit, providing <strong>a cosy place to have a rest and print custom VegFest merchandise or get crafty with paint and cardboard</strong>.
  </p>
  <p>
We ended the day in <strong>Betty Nicols, who hosted our afterparty and put on their debut vegan buffet</strong> full of delicious fresh sandwiches, not-sausage rolls, pizza and breaded cauliflower. It was an incredible spread and disappeared quickly despite being served immediately after a food festival!
  </p>
  <p>
We're extremely grateful to the Council's bin team, who made sure the street was immaculate both before and after the market. And to the three visitors who appeared out of nowhere to hold onto a gazebo in a moment of need. Thanks also to Colin Salmond-Wallace for being a supporter of the event and a great ally on the High Street.
  </p>
  <p>
<strong>And our indispensable volunteers</strong> - Alex, Kirstin, Jonathan, Catherine, Rikki, James - who went above and beyond to monitor traffic, check on vendors, look after VegFest HQ, set up and pack down, work in the Creative Space, and run errands in the rain; and to Effie our photographer. The Plant-Based Fife team went above and beyond, diving in wherever needed at a moment's notice throughout the day.
  </p>
  <p>
<strong>We'll be back in autumn for VegFest 4, on Sunday 19th October. Keep an eye out for more updates.</strong>
  </p>
  <p>
In the meantime, look out for our VegFest Socials and fundraisers, and we welcome any <a href="https://vgn.kdy.scot/funds">donations that will help us to meet our costs</a> for this and future events.
  </p>
  <p>
With love and admiration,
  </p>
  <p>
<em>Team VegFest (Amy, Lea, Abi, Kit, Tony and Shiva)</em>
  </p>
