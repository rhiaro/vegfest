<h2 class="red" id="creativespace">
Creative Space
</h2>
<p>The VegFest Creative Space is hosted by <a href="https://penpals.site">Pen Pals</a> with a theme of <strong>sustainability in creative practice</strong>. Find us in the back room of the Old Kirk.</p>

<p>Come along for guided sustainable art workshops, DIY t-shirt printing (book a spot using the links below to guarantee your place), or drop-in any time for self-guided creative activities and friendly chat about how to make your creative practice more environmentally friendly.</p>
<p>Scheduled workshops:</p>
<ul>
  <li><strong>11:30 – 12:00 DIY merch printing:</strong> Bring fabric to print! <a href="https://www.tickettailor.com/events/penpals/1862111">Book a spot here</a>.</li>
  <li><strong>12:30 – 13:30 Visible mending with Lauren:</strong> Explore some of the basic stitches and techniques for mending your own clothes. Lauren hosts a regular mending space in Kirkcaldy! <a href="https://www.tickettailor.com/events/penpals/1862122">Book a spot here</a>.</li>
  <li><strong>14:00 – 15:00 Sun printing with Yarrow:</strong> Capture the waning light into beautiful images of plants and rocks. If it's raining, we'll be inside making prints with leaves and feathers. <a href="https://www.tickettailor.com/events/penpals/1862134">Book a spot here</a>.</li>
  <li><strong>15:30 – 16:00 DIY merch printing:</strong> Bring fabric to print! <a href="https://www.tickettailor.com/events/penpals/1862146">Book a spot here</a>.</li>
</ul>
<p><strong>DIY merch printing</strong>: <strong>Bring your own</strong> t-shirt, tote bag, tea towel or any other fabric to print, and try to come at the start of the session for an introduction to what to do! Printing is pay-as-you-feel to help us cover the costs of materials.</p>
<p>And drop in any time for...</p>
<ul>
  <li><strong>Gentle crafting:</strong> self-guided with some materials provided.</li>
  <li><strong>The Wee Vegan Library:</strong> For everyone to browse. Pick up a book about cooking, climate, health and wellbeing and more.</li>
</ul>
<p>Children are welcome in the creative space, but must be supervised by a responsible adult at all times!</p>
<p>Everything is <strong>free to access</strong> thanks to support from FCCAN and the Fife Climate Festival!</p>
